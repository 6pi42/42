#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*ptr;
	int		i;

	i = 0;
	ptr = NULL;
	ptr = (char*)s;
	while (n--)
	{
		ptr[i] = (char)c;
		i++;
	}
	return (s);
}
