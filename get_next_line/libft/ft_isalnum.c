#include "libft.h"

int	ft_isalnum(int c)
{
	if (!((c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9')
		|| (c >= 'a' && c <= 'z')))
		return (0);
	return (1);
}
